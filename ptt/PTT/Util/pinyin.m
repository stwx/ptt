@implementation NSString (enumrateCharater)

- (void)enumerateCharaterUsingBlock:(void(^)(unsigned short letter, BOOL *stop))enumertae
{
    static BOOL stop;
    for (NSInteger i = 0; i < [self length]; i++)
    {
        enumertae([self characterAtIndex:i], &stop);
        if (stop) break;
    }
}
