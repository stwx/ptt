+(BOOL)searchResult:(NSString *)name searchText:(NSString *)searchT{
    NSComparisonResult result =
    [name compare:searchT options:NSCaseInsensitiveSearch
                            range:NSMakeRange(0, searchT.length)];
    if (result == NSOrderedSame)
        return YES;
    else
        return NO;
}



